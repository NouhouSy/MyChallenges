#13. Utilisez l'opérateur _and_ pour vérifier si 'on' se trouve à la fois dans 'python' et 'dragon'.
var1 = "python"
var2 = "dragon"
if "on" in var1 and "on" in var2:
    print(" 'on' se trouve à la fois dans 'Python' et 'Dragon' ")
else:
    print(" 'on' ne se trouve pas dans 'Python' et 'Dragon'")