#14. _I hope this course is not full of jargon_. Utilisez l'opérateur _in_ pour vérifier si _jargon_ est dans la phrase.
chaine = "I hope this course is not full of jargon"
if "jargon" in chaine:
    print("Le mot 'jargon' existe bien dans la phrase.")
else:
    print("Ce mot ne se trouve pas dans la phrase")